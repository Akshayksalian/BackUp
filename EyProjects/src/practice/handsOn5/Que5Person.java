package practice.handsOn5;

public class Que5Person {
	
	private int id;
	private String name;
	
	Que5Person(int id,String name){
		this.id = id;
		this.name = name;
	}
	
	Que5Person() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
